// For loops

fun main() {
    val planets= arrayOf("earth", "venus", "neptune") //string arrays
    for (i in planets) println("$i")
    for (i in 1 .. 5) println("hello")
    for (i in 1 until 5) println("wow")
    val planets= arrayOf("earth", "venus", "neptune") //string arrays
    for (i in 1 .. 5) println("hello")
    for (i in 1 until 5) println("wow")
    for (i in planets) println("$i")
}
